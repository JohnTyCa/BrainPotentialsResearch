% Calculate Cohen's D measure of effect size giving the data from two
% groups.
% 
% ======================================================================= %
% Required Inputs:
% ======================================================================= %
%
% group1    -   Nx1 array for group 1.
% group2    -   Nx1 array for group 2.
%
% ======================================================================= %
% Optional Inputs:
% ======================================================================= %
% 
% ======================================================================= %
% Outputs:
% ======================================================================= %
%
% d     -   Cohen's D.
% 
% ======================================================================= %
% Example
% ======================================================================= %
%
% group1 = rand(25,1);
% group2 = rand(25,1);
% 
% % Small Effect Size
% 
% d = CohensD(group1,group2);
% 
% % Large Effect Size;
% 
% d = CohensD(group1*2,group2);
% 
% ======================================================================= %
% Dependencies.
% ======================================================================= %
% 
% ======================================================================= %
% UPDATE HISTORY:
%
% 12/08/2019 (v1.0) -   V1.0 Created.
%
% ======================================================================= %

function d = CohensD(group1,group2)

M1 = mean(group1);
M2 = mean(group2);
S1 = std(group1);
S2 = std(group2);
SPooled = sqrt( ((S1^2) + (S2^2)) / 2);
d = (M2 - M1) / SPooled;

d = abs(d);

end