function wait(deltatime); 

m=cogstd('sGetTime',-1);
s=m;
disp(['timer started at ' num2str(m) ' sec']);
while m<s+deltatime
    m=cogstd('sGetTime',-1);
end;
%disp(['left after ' num2str(m-s) ' sec']);