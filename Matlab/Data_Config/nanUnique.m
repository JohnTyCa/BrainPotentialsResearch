% Extract unique numbers in an array that has NaN values. The MATLAB
% built-in function 'unique' will return the number of instances that NaN
% was found.
% 
% ======================================================================= %
% Required Inputs:
% ======================================================================= %
%
% x     -   Data array.
%
% ======================================================================= %
% Optional Inputs:
% ======================================================================= %
%
% 
% 
% ======================================================================= %
% Outputs:
% ======================================================================= %
%
% y     -   Unique values.
% 
% ======================================================================= %
% Example
% ======================================================================= %
%
% y = nanUnique([1 1 2 2 3 3 4 5 6 NaN NaN NaN]);
% 
% ======================================================================= %
% Dependencies.
% ======================================================================= %
% 
% 
% 
% ======================================================================= %
% UPDATE HISTORY:
%
% 30/1/2018 (v1.0) -   V1.0 Created.
%
% ======================================================================= %

function y = nanUnique(x)
y = unique(x);
try
    if any(isnan(y))
        y(isnan(y)) = []; % remove all nans
        y(end+1) = NaN; % add the unique one.
    end
catch
end
end