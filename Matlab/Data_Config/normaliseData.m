% Normalise an array between X and Y.
% 
% ======================================================================= %
% Required Inputs:
% ======================================================================= %
%
% data  -   Data array.
% x     -   Lower bound.
% y     -   Upper bound.
%
% ======================================================================= %
% Optional Inputs:
% ======================================================================= %
% 
% ======================================================================= %
% Outputs:
% ======================================================================= %
%
% normalised    -   Normalised data.
% 
% ======================================================================= %
% Example
% ======================================================================= %
%
% normalized = normaliseData(0:0.1:1,0,50);
% 
% ======================================================================= %
% Dependencies.
% ======================================================================= %
% 
% ======================================================================= %
% UPDATE HISTORY:
%
% 15/10/2018 (v1.0) -   V1.0 Created.
% 24/03/2020 (v1.1) -   Handles multi-dimensional arrays.
% 
% ======================================================================= %

function normalised = normaliseData(data,x,y)

MaxData = data;
MinData = data;
for iDim = 1:length(size(data))
    MaxData = max(MaxData);
    MinData = min(MinData);
end

normalised = x+(y-x)*(data-MinData)/( MaxData-MinData);

end