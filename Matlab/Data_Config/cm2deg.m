% Convert cm distance into degrees of visual angle based on a certain
% viewing distance.
% 
% ======================================================================= %
% Required Inputs:
% ======================================================================= %
%
% cm                -   Distance in cm.
% ViewingDistance   -   Viewing distance.
%
% ======================================================================= %
% Optional Inputs:
% ======================================================================= %
% 
% ======================================================================= %
% Outputs:
% ======================================================================= %
%
% deg   -   Degrees of visual angle expressed by cm.
% 
% ======================================================================= %
% Example
% ======================================================================= %
%
% cm = 8;
% ViewingDistance = 80;
% 
% deg = cm2deg(cm,ViewingDistance);
% 
% ======================================================================= %
% Dependencies.
% ======================================================================= %
% 
% ======================================================================= %
% UPDATE HISTORY:
%
% 22/08/2019 (v1.0) -   V1.0 Created.
%
% ======================================================================= %

function deg = cm2deg(cm,ViewingDistance)

deg = rad2deg(2 * (atan((cm / 2) / ViewingDistance)));

end